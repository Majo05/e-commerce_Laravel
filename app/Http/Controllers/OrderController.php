    public function add($id)
      {
        $product =  Product::find($id);
        $product = [
              'id' => $product->id,
              "name" => $product->name,
              'description' => $product->description,
              'category_id' => $product->category_id,
              'price' => $product->price,
              'image' => $product->image,
        ];

         session()->put("user.order." . $id, $product);


         return view('order');
      }

      public function remove($id)
      {
          session()->pull('user.order.' . $id, "default");
          return view('order');
      }

